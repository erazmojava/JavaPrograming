public class Tacka {

    private double xOsa;
    private double yOsa;

    public Tacka(double xOsa, double yOsa) {
        this.xOsa = xOsa;
        this.yOsa = yOsa;
    }

    public double getxOsa() {
        return xOsa;
    }

    public void setxOsa(double xOsa) {
        this.xOsa = xOsa;
    }

    public double getyOsa() {
        return yOsa;
    }

    public void setyOsa(double yOsa) {
        this.yOsa = yOsa;
    }
}
